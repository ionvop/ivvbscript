public class Print {
    public void print(String input) {
        System.out.println(input);
    }

    public void print(int input) {
        System.out.println(input);
    }

    public void print(float input) {
        System.out.println(input);
    }

    public void print(Boolean input) {
        System.out.println(input);
    }

    public void breakPoint(String input) {
        print(input);
        System.exit(0);
    }

    public void breakPoint(int input) {
        print(input);
        System.exit(0);
    }

    public void breakPoint(float input) {
        print(input);
        System.exit(0);
    }

    public void breakPoint(Boolean input) {
        print(input);
        System.exit(0);
    }
}
